#!/usr/bin/node

// Comparison operators are used to compare values
// and return a boolean value
// True or False

const a = 2;
const b = 3;

console.log(a > b); // greater than
console.log(a >= b); // greater than or equal to
console.log(a < b); // less than
console.log(a <= b); // less than or equal to
console.log(a === b); // equal to (compares value  only)
console.log(a === b); // equal to (compares value and  data type)
console.log(a !== b); // not equal to (compares value  only)
console.log(a !== b); // not equal to (compares value  and data type)
